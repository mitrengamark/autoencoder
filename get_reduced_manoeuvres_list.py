            with open(json_path, "r") as f:
                data = json.load(f)

            # Az eltávolítandó manőverek összegyűjtése (összes kulcs alá tartozóból)
            to_remove = set()
            for group in data.values():
                to_remove.update(group)

            if "OG_remake" in file:
                input_list = all_manoeuvres
